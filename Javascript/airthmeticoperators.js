let a = 25; // number
let b = 10; // number

console.log("a + b = ", (a + b)); // here it adds a and b
console.log("a - b = ", (a - b));
console.log("a * b = ", (a * b));
console.log("a / b = ", (a / b));
console.log("a % b = ", (a % b)); // when 25 is divided by 10 leaves remainder = 5

let c = '25'; // string
let d = 10; // number

console.log("c + d = ", (c + d)); // here it appends c and d
console.log("c - d = ", (c - d)); // here c will be internally converted to a number and then subtraction happens
console.log("c * d = ", (c * d)); // here c will be internally converted to a number and then multiplication happens
console.log("c / d = ", (c / d)); // here c will be internally converted to a number and then division happens
console.log("c % d = ", (c % d)); // here c will be internally converted to a number and then modulus operation happens
